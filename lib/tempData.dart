List<List<(int, double)>> generateMinuteData() {
  DateTime now = DateTime.now();
  Random random = Random();
  List<List<(int, double)>> minuteData = [];

  for (int i = 0; i < 24; i++) {
    List<(int, double)> hourData = [];

    for (int j = 0; j < 60; j += 10) {
      DateTime minute = DateTime(now.year, now.month, now.day, i, j);
      int timestamp = minute.millisecondsSinceEpoch ~/ 1000;
      double value = 1.0 + random.nextDouble() * 14.0; // 生成1到15之间的随机数

      hourData.add((timestamp, value));
    }

    minuteData.add(hourData);
  }

  return minuteData;
}

